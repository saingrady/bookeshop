package org.benassi.bookeshop.data.model;

import java.text.DecimalFormat;
import java.util.Date;

/**
 * Book domain object. To keep it simple :
 * Use only lead author instead authors list
 * Store the stock details (stock, price, offer) instead of separate table BookStock
 */
public class Book {

    private String isbn;

    private String title;

    private Date publishDate;

    private Author author;

    private Category category;

    private int stock;

    private float price;

    private int offer;

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Author getAuthor() {
        return author;
    }

    public void setAuthor(Author author) {
        this.author = author;
    }

    public Date getPublishDate() {
        return publishDate;
    }

    public void setPublishDate(Date publishDate) {
        this.publishDate = publishDate;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public int getOffer() {
        return offer;
    }

    public void setOffer(int offer) {
        this.offer = offer;
    }

    public double getDiscountPrice(){
        DecimalFormat df = new DecimalFormat("###.##");
        return Double.valueOf(df.format(price - price * offer/100));
    }

}
